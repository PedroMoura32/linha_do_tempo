        let processedText = text;
        const markdownRegex = /\[([^\]]+)\]\((https?:\/\/[^)]+)\)/g;
        processedText = processedText.replace(markdownRegex, '<a href="$2" target="_blank" rel="noopener noreferrer">$1</a>');
